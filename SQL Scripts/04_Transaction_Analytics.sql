--9.PURPOSE ANALYSIS
SELECT
    CASE
        WHEN K_SYMBOL = 'UVER' THEN 'Loan Payment'
        WHEN K_SYMBOL = 'UROK' THEN 'Interest'
        WHEN K_SYMBOL = 'SIPO' THEN 'Utility Payment'
        WHEN K_SYMBOL = 'POJISTNE' THEN 'Insurance'
        WHEN K_SYMBOL = 'SLUZBY' THEN 'Service Charges'
        WHEN K_SYMBOL = 'DUCHOD' THEN 'Pension'
        WHEN K_SYMBOL = 'SANKC. UROK' THEN 'Penalty Interest'
        ELSE NVL(K_SYMBOL, 'Unknown')
    END AS PURPOSE,
    COUNT(*) AS TRANSACTION_COUNT,
    SUM(AMOUNT) AS TOTAL_AMOUNT
FROM TRANSACTIONS
GROUP BY K_SYMBOL
ORDER BY TOTAL_AMOUNT DESC;

--10.TOP ACCOUNTS
SELECT *
FROM
(
SELECT ACCOUNT_ID, SUM(AMOUNT) TOTAL_AMOUNT,
RANK() OVER (ORDER BY SUM(AMOUNT) DESC) RNK
FROM TRANSACTIONS
GROUP BY ACCOUNT_ID
)
WHERE RNK<=10;