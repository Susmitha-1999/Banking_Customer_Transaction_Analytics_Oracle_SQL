--5.TRANSACTION OVERVIEW
SELECT COUNT(*) AS  TOTAL_TRANSACTIONS,
SUM(AMOUNT)AS TOTAL_AMOUNT
FROM TRANSACTIONS;

--6.TRANSACTION TYPE ANALYSIS
SELECT
    CASE
        WHEN TYPE = 'PRIJEM' THEN 'Credit'
        WHEN TYPE = 'VYDAJ' THEN 'Debit'
        WHEN TYPE = 'VYBER' THEN 'Cash Withdrawal'
        ELSE TYPE
    END AS TRANSACTION_TYPE,
    COUNT(*) AS TRANSACTION_COUNT,
    SUM(AMOUNT) AS TOTAL_AMOUNT
FROM TRANSACTIONS
GROUP BY TYPE;

--7.OPERATION ANALYSIS
SELECT
    CASE
        WHEN OPERATION = 'VKLAD' THEN 'Cash Deposit'
        WHEN OPERATION = 'VYBER' THEN 'Cash Withdrawal'
        WHEN OPERATION = 'PREVOD Z UCTU' THEN 'Transfer From Account'
        WHEN OPERATION = 'PREVOD NA UCET' THEN 'Transfer To Account'
        WHEN OPERATION = 'VYBER KARTOU' THEN 'Card Withdrawal'
        ELSE NVL(OPERATION, 'Unknown')
    END AS OPERATION,
    COUNT(*) AS TRANSACTION_COUNT,
    SUM(AMOUNT) AS TOTAL_AMOUNT
FROM TRANSACTIONS
GROUP BY OPERATION
ORDER BY TOTAL_AMOUNT DESC;

--8.MONTHLY TREND
SELECT TO_CHAR(TRANS_DATE,'YYYY-MM') MONTH,
COUNT(*) AS TRANSACTIONS,
SUM(AMOUNT) AS AMOUNT
FROM TRANSACTIONS
GROUP BY TO_CHAR(TRANS_DATE,'YYYY-MM')
ORDER BY MONTH;

